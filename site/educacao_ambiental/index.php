        <p class="blog-post-meta">02/07/2021</p>

        <p>A pesquisadora Tereza Coni Aguiar, em seu artigo científico 
        “Uma visão integrada dos impactos ambientais no estado do Rio de Janeiro” 
        explana que: “A região Norte Fluminense, considerada uma das mais problemáticas 
        do estado do Rio de Janeiro, apresenta inúmeros problemas que advêm dos impactos 
        provenientes das atividades relacionadas à exploração do petróleo, à implantação 
        de grandes empreendimentos e à expansão desordenada de suas cidades”. 
        Diante desse preocupante cenário, urge a necessidade de se observar mais atentamente
        para a movimentação ambiental de nossa região bem como propor medidas que venham 
        fechar as brechas na saúde do meio ambiente.
</p>
        <hr class="text-primary">
        
        <h2 class="text-right">Beira Valão - Centro</h2>
        <p class="text-left">A maior obra  de engenharia  do século XIX</p>
        <blockquote class="blockquote">
          <p>Vamos lá !?</p>
        </blockquote>
        <p>A ideia de sua construção surgiu pela primeira vez numa obra escrita
        pelo bispo José Joaquim de Azeredo Coutinho em 1794. O religioso defendia
        uma ligação entre os rios Macaé e Paraíba do Sul, passando pelos rios Ururaí
        e Macabu e pelas lagoas Feia e de Carapebus, para facilitar o transporte de
        produtos agrícolas entre Campos e Macaé, que era feito em grande parte por
        carros de boi. Uma travessia por canal também eliminaria os perigos da navegação
        pelo litoral: eram comuns os naufrágios na foz do Rio Paraíba a caminho do Porto
        do Imbetiba.</p>
        
        <h4>Poluição e Eutrofização no foco d`água</h4>
        <p>Nos últimos anos, com a grande urbanização do município e dos seus arredores,
        o município de Campos sofre com despejo de esgoto e lixo no Canal Campos-Macaé, 
        o que com o passar do tempo acabou dando juz ao nome que usamos hoje, Beira Valão.
        Antes um canal que foi construído para facilitar o escoamento da produção açucareira
        e também o transporte de pessoas, hoje vemos que entrou em desuso, é usado de forma
        inadequada e transporta outras coisas:  
        </p>
        <ul>
          <li><a href="http://g1.globo.com/rj/norte-fluminense/noticia/2015/06/canal-campos-macae-rj-ainda-incomoda-moradores-e-especialistas.html">Mau cheiro</a> na avenida ao entorno</li>
          <li>Despejo Inadequado do lixo doméstico</li>
          <li><a href="https://www.jornalterceiravia.com.br/2021/03/12/corpo-de-um-homem-e-encontrado-boiando-no-canal-campos-macae/">Cena de Crime</a></li>
        </ul>
        
        <h2>Alagamentos Pós Chuvas - Pelinca</h2>
        <p>O vídeo amador abaixo é uma frequente indignação do povo campista. Não só esse
        como outros vivem reclamando dos alagamentos em períodos de chuva na região.
        Além da falta de gestão por ele mencionada, há um problema social envolvido, visto que
        as pessoas em geral, não tem o hábito de jogar o lixo no seu devido lugar.
        </p>
        
        
        <span class="border border-primary">
